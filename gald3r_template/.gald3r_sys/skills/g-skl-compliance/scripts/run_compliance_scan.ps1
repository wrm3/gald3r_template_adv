COMPLIANCE: PASS (stub — no real scan performed)
"@

$stubReport | Set-Content -Path $reportPath -Encoding UTF8
Write-Host "g-skl-compliance: report written to $reportPath"
Write-Host "g-skl-compliance: COMPLIANCE: PASS (stub)"

exit 0
